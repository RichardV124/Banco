	private void btnCrearCreditActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnCrearCreditActionPerformed
		// TODO add your handling code here:

		try {

			if (tfIdCustomerCredit.getText().isEmpty() || tfMontoCredit.getText().isEmpty()
					|| cbFranquisia.getSelectedIndex() == 0) {
				JOptionPane.showMessageDialog(null, "Debe completar todos los campos");
			} else {

				Customer c = controladorCus.buscarCustomer((String) cbTipoDocumentoCredit.getSelectedItem(),
						tfIdCustomerCredit.getText());
				int cant = 0;
				if (c != null) {
					for (Product lista : c.getProductos()) {
						if (lista instanceof CreditCard) {
							cant++;
						}
					}
					if (c.getProductos().size() >= 5) {
						JOptionPane.showMessageDialog(null, "Este cliente ya tiene 5 productos registrados");
					} else if (cant == 2) {
						JOptionPane.showMessageDialog(null, "Este cliente ya tiene 2 tarjetas de credito");
					} else {
						CreditCard credit = new CreditCard();
						credit.setFranchise((Franchise) cbFranquisia.getSelectedItem());
						credit.setCustomer(c);
						credit.setAmmount(Double.parseDouble(tfMontoCredit.getText()));
						credit.setAmmountConsumed(0);
						controlador.crearCreditCard(credit);
						JOptionPane.showMessageDialog(null, "Se creo correctamente la tarjeta de credito");
					}
				} else {
					JOptionPane.showMessageDialog(null, "El customer con numero de identificacion "
							+ tfIdCustomerCredit.getText() + " no esta " + "registrado en el banco");
				}
			}
		} catch (NumberFormatException e) {
			JOptionPane.showMessageDialog(null, "El monto debe ser un valor numerico", "ERROR",
					JOptionPane.ERROR_MESSAGE);
		} catch (Exception e) {
			e.printStackTrace();
			JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
		}
	}// GEN-LAST:event_btnCrearCreditActionPerformed

