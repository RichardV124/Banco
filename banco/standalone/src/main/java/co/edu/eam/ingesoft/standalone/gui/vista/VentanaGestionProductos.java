	private void btnTransaccionActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnTransaccionActionPerformed
		// TODO add your handling code here:

		try {

			if (cbCuentaTransaccion.getSelectedIndex() == 0) {
				JOptionPane.showMessageDialog(null,
						"Debe seleccionar la cuenta de ahorros de la cual" + " quiere transferir");
			} else if (cbCuentaATransferir.getSelectedIndex() == 0) {
				JOptionPane.showMessageDialog(null, "Debe seleccionar a la cual va a transferir");
			} else if (cbCuentaTransaccion.getSelectedIndex() == cbCuentaATransferir.getSelectedIndex()) {
				JOptionPane.showMessageDialog(null, "No puede transferir saldo entre la misma cuenta");
			} else if (tfMontoTransaccion.getText().isEmpty()) {
				JOptionPane.showMessageDialog(null, "Debe ingresar el monto a transladar");
			} else {

				SavingAccount savSalida = (SavingAccount) cbCuentaTransaccion.getSelectedItem();
				SavingAccount savEntrada = (SavingAccount) cbCuentaATransferir.getSelectedItem();
				controlador.transladarMontoCuenta(savSalida, savEntrada,
						Double.parseDouble(tfMontoTransaccion.getText()));
				JOptionPane.showMessageDialog(null, "Se consigno correctamente el dinero a su cuenta de ahorros");
				limpiarCamposConsignar();
			}

		} catch (NumberFormatException e) {
			JOptionPane.showMessageDialog(null, "El monto a transladar debe ser un valor numerico", "ERROR",
					JOptionPane.ERROR_MESSAGE);
		} catch (Exception e) {
			e.printStackTrace();
			JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
		}

	}// GEN-LAST:event_btnTransaccionActionPerformed

