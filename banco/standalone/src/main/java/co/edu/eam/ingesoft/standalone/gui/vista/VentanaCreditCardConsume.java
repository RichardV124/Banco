	/**
	 * Metodo para cargar un combo con todas las tarjetas de credito de un
	 * cliente en especifico
	 * 
	 * @param c
	 *            cliente por el cual se listaran las tarjetas de credito
	 */
	public void cargarComboCuentas(Customer c) {
		cbNumeroTarjetaAvance.removeAllItems();
		cbNumeroTarjetaPedir.removeAllItems();
		List<CreditCard> cred = controlador.listarTarjetasCliente(c);
		cbNumeroTarjetaAvance.addItem("Seleccione una tarjeta");
		cbNumeroTarjetaPedir.addItem("Seleccione una tarjeta");
		for (CreditCard lista : cred) {
			cbNumeroTarjetaAvance.addItem(lista);
			cbNumeroTarjetaPedir.addItem(lista);
		}
	}

