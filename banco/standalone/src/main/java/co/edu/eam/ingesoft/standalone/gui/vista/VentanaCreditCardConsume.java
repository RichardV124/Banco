    private void btnPedirAvanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPedirAvanceActionPerformed
        // TODO add your handling code here:
        try {

        	if(tfMontoAvance.getText().isEmpty()){
				JOptionPane.showMessageDialog(null, "Debe diligenciar todos los datos para hacer el avance");
			}else{
			CreditCardConsume creditConsume = new CreditCardConsume();
			CreditCard credit = (CreditCard) cbNumeroTarjetaPedir.getSelectedItem();
			creditConsume.setPayed(false);
			creditConsume.setCreditCard(credit);
			creditConsume.setInterest(0.036);
			creditConsume.setRemainingAmmount(Double.parseDouble(tfMontoAvance.getText()));
			creditConsume.setAmmount(Double.parseDouble(tfMontoAvance.getText()));
			creditConsume.setNumberShares(24);
			controlador.crearCreditCardConsume(creditConsume);
			controlador.editarCreditCard(credit, Double.parseDouble(tfMontoAvance.getText()));
			JOptionPane.showMessageDialog(null, "Se pidio el avance correctamente");
			}
		} catch (Exception e) {
			e.printStackTrace();
			JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
		}
    }//GEN-LAST:event_btnPedirAvanceActionPerformed

	private void btnBuscarClienteActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnBuscarClienteActionPerformed
		// TODO add your handling code here:
		try {
			Customer cus = controladorCus.buscarCustomer((String) cbTipoDocumento.getSelectedItem(),
					tfNumeroDocumento.getText());
			if (cus != null) {

				cargarComboCuentas(cus);
				jTBPrincipal.setVisible(true);

			} else {
				JOptionPane.showMessageDialog(null, "No existe!!!!", "ERROR", JOptionPane.WARNING_MESSAGE);
			}

		} catch (Exception e) {
			e.printStackTrace();
			JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
		}

	}// GEN-LAST:event_btnBuscarClienteActionPerformed

	private void btnPedirConsumoActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnPedirConsumoActionPerformed
		// TODO add your handling code here:
		try {

			if(tfMontoPedir.getText().isEmpty() || tfNumeroCuotasPedir.getText().isEmpty()){
				JOptionPane.showMessageDialog(null, "Debe diligenciar todos los datos para hacer el consumo");
			}else{
			CreditCardConsume creditConsume = new CreditCardConsume();
			CreditCard credit = (CreditCard) cbNumeroTarjetaPedir.getSelectedItem();
			creditConsume.setPayed(false);
			creditConsume.setCreditCard(credit);
			creditConsume.setInterest(0.036);
			creditConsume.setRemainingAmmount(Double.parseDouble(tfMontoPedir.getText()));
			creditConsume.setAmmount(Double.parseDouble(tfMontoPedir.getText()));
			creditConsume.setNumberShares(Integer.parseInt(tfNumeroCuotasPedir.getText()));
			controlador.crearCreditCardConsume(creditConsume);
			controlador.editarCreditCard(credit, Double.parseDouble(tfMontoPedir.getText()));
			JOptionPane.showMessageDialog(null, "Se pidio el consumo correctamente");
			}
		} catch (Exception e) {
			e.printStackTrace();
			JOptionPane.showMessageDialog(null, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
		}
	}// GEN-LAST:event_btnPedirConsumoActionPerformed

